    private void showPlaceHolderPopup()
    {
        try
        {
            Rectangle pos = messageTemplateArea.modelToView(messageTemplateArea.getCaretPosition());
            templatePopupMenu.show(messageTemplateArea, ((Double) pos.getX()).intValue(), ((Double) pos.getY()).intValue() + pos.height);
        }
        catch (BadLocationException ex)
        {
            ex.printStackTrace();
        }
    }

    private void processPlaceHolderAction(ActionEvent event)
    {
        String field = event.getActionCommand();
        switch (field)
        {
            case "Account Number":
                field = "{ACCTNO}";
                break;
            case "Contra Account Number":
                field = "{CONTRAACCT}";
                break;
            case "Customer Name":
                field = "{CUSTNAME}";
                break;
            case "Transaction Amount":
                field = "{TXNAMT}";
                break;
            case "Transaction Charge":
                field = "{CHARGE}";
                break;
            case "Transaction Description":
                field = "{TXNDESC}";
                break;
            case "Cleared Balance":
                field = "{CLRDBAL}";
                break;
            case "Transaction Date":
                field = "{TXNDATE}";
                break;
            case "Transaction Currency":
                field = "{CURRENCY}";
                break;
            case "Transaction Originator":
                field = "{ORIGINATOR}";
                break;
            case "Days Due or Days Past":
                field = "{DAYS}";
                break;
            case "Access Code":
                field = "{ACSCODE}";
                break;
            case "Loan Due Date":
                field = "{DUEDATE}";
                break;
            case "Repayment Amount":
                field = "{REPAYAMT}";
                break;
            case "Unpaid Amount":
                field = "{UNPAIDAMT}";
                break;
            case "Mobile PIN":
                field = "{PIN}";
                break;
        }
        String text = messageTemplateArea.getText();
        int carPos = messageTemplateArea.getCaretPosition();
        String tSp = "", lSp = text.substring(0, carPos).endsWith(" ") ? "" : " ";
        if (carPos < text.length())
        {
            tSp = text.substring(carPos).startsWith(" ") ? "" : " ";
        }
        messageTemplateArea.insert(lSp + field + tSp, carPos);
    }

