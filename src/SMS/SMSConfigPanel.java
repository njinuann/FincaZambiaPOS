    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_saveButtonActionPerformed
    {//GEN-HEADEREND:event_saveButtonActionPerformed
        // TODO add your handling code here:
        if (alertType.getSelectedIndex() == 0)
        {
            JOptionPane.showMessageDialog(null, "Alert Type required.");
        }
        else if (templateIDField.getText().trim().length() == 0)
        {
            JOptionPane.showMessageDialog(null, "Template Id required.");
        }
        else if (messageTemplateArea.getText().trim().length() == 0)
        {
            JOptionPane.showMessageDialog(null, "Description required.");
        }
        else if (moduleBox.getSelectedIndex() == 0)
        {
            JOptionPane.showMessageDialog(null, "Module required.");
        }
        else if (chargeAmtField.getText().trim().length() == 0)
        {
            JOptionPane.showMessageDialog(null, "charge required.");
        }
        else if (chargeLedgerField.getText().trim().length() == 0)
        {
            JOptionPane.showMessageDialog(null, "Account Number required.");
        } else if (descriptionField.getText().trim().length() == 0)
        {
            JOptionPane.showMessageDialog(null, "Description required.");
        }
        else
        {
            sMSTemplate.setTemplateId(templateIDField.getText().trim());
            sMSTemplate.setCurrency(currencyBox.getSelectedItem().toString());

            sMSTemplate.setTemplateMsg(messageTemplateArea.getText().trim().replaceAll("'", "''"));
            sMSTemplate.setModule(moduleBox.getSelectedItem().toString());

            sMSTemplate.setStatus(statusBox.getSelectedItem().toString());
            sMSTemplate.setModifiedBy(EILogin.getAdminName());

            sMSTemplate.setDateModified(new Date());
            sMSTemplate.setChargeLedger(chargeLedgerField.getText().trim());

            sMSTemplate.setChargeDesc(chargeDescField.getText().trim());
            sMSTemplate.setChargeAmt(new BigDecimal(chargeAmtField.getText()));

            sMSTemplate.setTaxledger(taxLedgerField.getText().trim());
            sMSTemplate.setTaxDesc(taxDescField.getText().trim());

            sMSTemplate.setTaxAmt(new BigDecimal(taxAmtField.getText()));
            sMSTemplate.setTaxPerc(new BigDecimal(taxPercField.getText()));

            sMSTemplate.setIsChargable(chargableBox.isSelected() ? "Y" : "N");
            sMSTemplate.setCurrency(currencyBox.getSelectedItem().toString());
            
            sMSTemplate.setAlertType(alertType.getSelectedItem().toString());
            sMSTemplate.setDescription(descriptionField.getText().trim());

            if (tDClient.upsertSMSAlert(sMSTemplate))
            {
                showSmsAlert(sMSTemplate.getTemplateId());
                JOptionPane.showMessageDialog(PHMain.phFrame, "Alert saved successfully.");
            }
            else
            {
                JOptionPane.showMessageDialog(PHMain.phFrame, "Failed to save Alert. Please refer to log for more details.", "Database Error", JOptionPane.ERROR_MESSAGE);
            }
        }
}//GEN-LAST:event_saveButtonActionPerformed

