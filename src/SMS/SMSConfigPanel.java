    private void verifyAlert()
    {
        String templateId = templateIDField.getText().trim();
        if (PHController.getSmsAlert().containsKey(templateId))
        {
            findField.setText(templateId);
            findBox.setSelectedIndex(0);
            searchAlert();
        }
    }

    public void searchAlert()
    {
        DefaultMutableTreeNode rootNode = ((DefaultMutableTreeNode) alertTypeTree.getModel().getRoot());
        DefaultMutableTreeNode newTerminal = new DefaultMutableTreeNode("Add Biller Code");

        rootNode.removeAllChildren();
        rootNode.add(newTerminal);

        boolean isAlertFound = false;
        for (SMSTemplate sTemplate : PHController.getSmsAlert().values())
        {
            if (findBox.getSelectedIndex() == 0 ? sTemplate.getTemplateId().endsWith(findField.getText().trim()) : sTemplate.getStatus().endsWith(findField.getText().trim()))
            {
                DefaultMutableTreeNode node = new DefaultMutableTreeNode(sTemplate);
                rootNode.add(node);

                if (!isAlertFound)
                {
                    alertTypeTree.setSelectionPath(new TreePath(node.getPath()));
                    isAlertFound = true;
                }
            }
        }

        terminalsScroller.getHorizontalScrollBar().setValue(0);
        terminalsScroller.getVerticalScrollBar().setValue(0);
        alertTypeTree.updateUI();
    }

    public void setAlertTree()
    {
        PHController.loadAlerts();
        DefaultMutableTreeNode smsAlertRootNode = ((DefaultMutableTreeNode) alertTypeTree.getModel().getRoot());
        DefaultMutableTreeNode newSmsAlert = new DefaultMutableTreeNode("Add Alert");

        smsAlertRootNode.removeAllChildren();
        smsAlertRootNode.add(newSmsAlert);

        PHController.getSmsAlert().values().stream().map((smsAlert) -> new DefaultMutableTreeNode(smsAlert)).forEach((node) ->
        {
            smsAlertRootNode.add(node);
        });

        terminalsScroller.getHorizontalScrollBar().setValue(0);
        terminalsScroller.getVerticalScrollBar().setValue(0);
        alertTypeTree.updateUI();
    }

    private void showSmsAlert(String ID)
    {
        findField.setText("");
        setAlertTree();

        DefaultMutableTreeNode rootNode = ((DefaultMutableTreeNode) alertTypeTree.getModel().getRoot());
        DefaultMutableTreeNode child = rootNode.getFirstLeaf();
        while (child != null)
        {
            if (child.getUserObject() instanceof SMSTemplate)
            {
                if (((SMSTemplate) child.getUserObject()).getTemplateId().equalsIgnoreCase(ID))
                {
                    alertTypeTree.setSelectionPath(new TreePath(child.getPath()));
                    alertTypeTree.updateUI();
                    return;
                }
            }
            child = child.getNextSibling();
        }
    }

