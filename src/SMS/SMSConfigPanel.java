    private void displayAlert()
    {
        DefaultMutableTreeNode selectedAlertNode = (DefaultMutableTreeNode) alertTypeTree.getLastSelectedPathComponent();
        if (selectedAlertNode != null)
        {
            Object selectedNode = selectedAlertNode.getUserObject();
            if ("Add Alert".equals(selectedNode))
            {
                sMSTemplate = new SMSTemplate();
                templateIDField.setEditable(true);
                templateIDField.setText("");
                messageTemplateArea.setText("");
                moduleBox.setSelectedIndex(0);
                statusBox.setSelectedIndex(0);
                modifiedByField.setText("");
                dateModifiedField.setText("");
                currencyBox.setSelectedIndex(0);
                chargeLedgerField.setText("");
                chargeDescField.setText("");
                chargeAmtField.setText("");
                taxLedgerField.setText("");
                taxDescField.setText("");
                taxAmtField.setText("");
                taxPercField.setText("");
                descriptionField.setText("");
                alertType.setSelectedIndex(0);
                chargableBox.setSelected(false);
                saveButton.setEnabled(true);
                saveButton.setText("Save");

            }
            else if (selectedNode instanceof SMSTemplate)
            {
                sMSTemplate = (SMSTemplate) selectedNode;
                templateIDField.setEditable(false);
                templateIDField.setText(sMSTemplate.getTemplateId());
                messageTemplateArea.setText(sMSTemplate.getTemplateMsg());
                moduleBox.setSelectedItem(sMSTemplate.getModule());
                statusBox.setSelectedItem(sMSTemplate.getStatus());
                modifiedByField.setText(sMSTemplate.getModifiedBy());
                currencyBox.setSelectedItem(sMSTemplate.getCurrency());
                chargeLedgerField.setText(sMSTemplate.getChargeLedger());
                chargeDescField.setText(sMSTemplate.getChargeDesc());
                chargeAmtField.setText(String.valueOf(sMSTemplate.getChargeAmt()));
                taxLedgerField.setText(sMSTemplate.getTaxledger());
                taxDescField.setText(sMSTemplate.getTaxDesc());
                taxAmtField.setText(String.valueOf(sMSTemplate.getTaxAmt()));
                taxPercField.setText(String.valueOf(sMSTemplate.getTaxPerc()));
                descriptionField.setText(sMSTemplate.getDescription());
                alertType.setSelectedItem(sMSTemplate.getAlertType());
                if (sMSTemplate.getIsChargable().equals("Y"))
                {
                    chargableBox.setSelected(true);
                }
                else
                {
                    chargableBox.setSelected(false);
                }

                dateModifiedField.setText(new SimpleDateFormat("EEE, d MMM yyyy HH:mm:ss a").format(sMSTemplate.getDateModified()));
                saveButton.setText("Update");
                saveButton.setEnabled(true);
            }
        }
    }

