    private void verifyAccount(String glNumber, JComponent jComponent)
    {
        BalloonTipStyle edgedLook = new EdgedBalloonStyle(Color.CYAN, Color.BLUE);
      //  BalloonTipStyle edgedLook2 = new EdgedBalloonStyle(Color.RED, Color.RED);
        Object[][] glLedger = tDClient.verifyGLNumber(glNumber.substring(9));
        if (glLedger.length != 0)
        {
            BalloonTip myBalloonTip = new BalloonTip(jComponent, new JLabel(tDClient.capitalize(glLedger[0][0].toString())), edgedLook, BalloonTip.Orientation.RIGHT_BELOW, BalloonTip.AttachLocation.ALIGNED, 40, 20, false);
            TimingUtils.showTimedBalloon(myBalloonTip, 3000);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Invalid or InActive Biller account!");
            // BalloonTip myBalloonTip = new BalloonTip(jComponent, new JLabel("Invalid GL or Inactive GL"), edgedLook2, BalloonTip.Orientation.RIGHT_BELOW, BalloonTip.AttachLocation.ALIGNED, 40, 20, true);
        }
    }

